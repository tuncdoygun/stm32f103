void MB_PrintPDU(uint8_t *pdu, uint8_t len)
{
  int i;
  
  for (i = 0; i < len; ++i)
    printf("%02X", pdu[i]);
  
  printf("\n");
}

// Geri dönüþ: 0 baþarýsýz
// Baþarýlý ise response PDU uzunluðu
int MB_Response(uint8_t *pmb_rsp_pdu)
{
  int i;
  uint16_t crc;
  
  if (_mb_rsp_len == 0)
    return 0;
  
  for (i = 0; i < _mb_rsp_len; ++i)
    _mb_rsp_adu[i] = FIFO_GetData(&_RxFifo);
  
  if (_mb_req_adu[0] != _mb_rsp_adu[0])
    return 0;
  
  if (_mb_req_adu[1] != (_mb_rsp_adu[1] & 0x7F))
    return 0;
  
  crc = CRC16((uint8_t *)_mb_rsp_adu, _mb_rsp_len);
  if (crc)
    return 0;

  memcpy(pmb_rsp_pdu, (void const *)(_mb_rsp_adu + 1), _mb_rsp_len - 3);
  return _mb_rsp_len - 3;
}

void Task_MB(void)
{
  static enum {
    S_INIT,
    S_WAIT_KEY,
    S_REQUEST,
    S_WAIT_RSP,
    S_RESPONSE,
  } state = S_INIT;
  
  static clock_t t0, t1;
  static uint8_t mb_req_pdu[SZ_PDU];
  static uint8_t mb_rsp_pdu[SZ_PDU];
  uint8_t mb_rsp_pdu_len;
  static unsigned long count;
  
  t1 = clock();

  switch (state) {
  case S_INIT:
    MB_Init();
    state = S_WAIT_KEY;
    break;
    
  case S_WAIT_KEY:
    if (g_Buttons[0]) {
      g_Buttons[0] = 0;
      
      state = S_REQUEST;
    }
    break;
    
  case S_REQUEST:
    _mb_rsp_len = 0;

    mb_req_pdu[0] = 0x03;   // Fonksiyon 3
                            // Read holding registers
    mb_req_pdu[1] = 0x00;
    mb_req_pdu[2] = 0x63;   // Addres: 0x0063 (99)
      // 40001 + adr = 40100

    mb_req_pdu[3] = 0x00;
    mb_req_pdu[4] = 0x0A;   // Length: 0x000A (10)
    
    OLED_SetCursor(1, 10);
    printf("%lu", ++count);
    OLED_SetCursor(2, 0);
    printf("(Q) ");
    MB_PrintPDU(mb_req_pdu, 5);
    
    if (MB_Request(MB_DEVICE, mb_req_pdu, 5)) {
      t0 = clock();
      state = S_WAIT_RSP;
    }
    break;
    
  case S_WAIT_RSP:
    if (_mb_rsp_len)
      state = S_RESPONSE;
    else if (t1 - t0 >= MB_TIMEOUT) {   
      OLED_SetCursor(3, 0);
      printf("(R) Timeout!");
      state = S_WAIT_KEY;
    }
    break;
    
  case S_RESPONSE:
    mb_rsp_pdu_len = MB_Response(mb_rsp_pdu);
    
    if (mb_rsp_pdu_len) {
      OLED_SetCursor(3, 0);
      printf("(R) ");
      MB_PrintPDU(mb_rsp_pdu, mb_rsp_pdu_len);
    }
    else {
      OLED_SetCursor(3, 0);
      printf("*ERROR**");
    }

    state = S_WAIT_KEY;
    break;
  }  
}

